#include <stdio.h>

int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	num;

	num = nb;
	i = 1;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (i < power)
	{
		nb *= num;
		i++;
	}
	return (nb);
}
